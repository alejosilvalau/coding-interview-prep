# O(1) time | O(1) space
# As n is max 32 bits integer, we drop the constant.
# Bit manipulation solution
class Solution:
    def reverseBits(self, n: int) -> int:
        maxBits = 32
        res = 0
        for i in range(maxBits):
            bit = (n >> i) & 1
            res += bit << ((maxBits - 1) - i)

        return res


